function readPath(tree: TranslationTree, path: string): TranslationValue | undefined {
  return path
    .split(".")
    .reduce<TranslationValue | undefined>((acc, key) => {
      if (!acc || typeof acc === "string" || Array.isArray(acc)) return undefined;
      return acc[key];
    }, tree);
}

export function createTranslator(language: LanguageCode) {
  return function t(path: string): string {
    const value = readPath(translations[language], path);
    if (typeof value === "string") return value;

    const fallback = readPath(translations.pl, path);
    if (typeof fallback === "string") return fallback;

    return path;
  };
}

export function getTranslationNode(path: string, language: LanguageCode): TranslationValue | undefined {
  const value = readPath(translations[language], path);
  if (value !== undefined) return value;
  return readPath(translations.pl, path);
}
